# 常量的定义
print(100)  # 这个100就是常量
print(3.1415926)  # 3.1415926也是一个常量

# 常量的使用
print(100 + 200 + 300)
print(3.14 * 10 * 10)  # 计算半径为10的 圆的面积

# 变量的定义
num1 = 100  # num1就是一个变量
num2 = 87  # num2也是一个变量
pi = 3.14  # pi也是一个变量

print(num1)
print(num2)
print(pi)

'''
在其他语言中
    有常量个变量的概念
    const a = 10
    
在python中其实是不明确区分变量与常量的
    看当前的这个值否可变
'''

# 变量在python中的作用
num1 = 100
num2 = 200
result = num1 + num2  # 将变量num1中的值 加上 num2中的值再给result变量
print(result)

